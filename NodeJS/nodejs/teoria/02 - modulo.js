/*

La modularidad es una técnica de diseño de software en la que un programa 
tiene partes diferenciadas, cada una de las cuales proporciona una única parte 
de la funcionalidad general.

Los módulos se unen para construir un todo cohesivo. La modularidad es 
esencial para crear programas escalables que incorporen bibliotecas y marcos 
y separen las preocupaciones del programa en fragmentos manejables. Básicamente,
un módulo es una colección de código ubicado en un archivo. En lugar de tener 
un programa completo ubicado en un solo archivo, el código se organiza en 
archivos separados según las preocupaciones que abordan. Estos archivos 
se pueden incluir en otros archivos mediante el uso de la require()función.

*/ 

