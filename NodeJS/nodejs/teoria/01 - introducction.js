/* DEFINICION NODEJS:

Node.js es un entorno de ejecución de JavaScript o un entorno 
que nos permite ejecutar código JavaScript fuera del navegador.

Un "entorno de ejecución" convierte el código escrito en un
lenguaje de programación de alto nivel , legible para humanos, y lo compila en un código que la computadora puede ejecutar.


NODO RPL:

REPL es una abreviatura de bucle de lectura , evaluación e impresión. 
Es un programa que recorre en bucle , o en ciclos repetidos, tres estados 
diferentes: un estado de lectura en el que el programa lee la entrada de 
un usuario, el estado de evaluación en el que el programa evalúa la entrada 
del usuario y el estado de impresión en el que el programa imprime su evaluación 
en una consola. Luego recorre en bucle estos estados nuevamente.

COMMAND in console:

node 

global

*/

